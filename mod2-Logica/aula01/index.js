/**
 *  > -> maior que 
 *  >=  -> maior ou igual
 *  =  -> atribuição
 *  < -> menor
 *  <= -> menor igual
 *  == -> igualdade(valor)
 *  === -> igualdade restrita *valor e tipo)
 *  != -> diferente
 *  !== -> diferente estrito(verifica valor e tipo)
 */

const num1 =10;
const num2 = '10';

const comp = num1 === num2; // verica o valor e o tipo
console.log(comp);


